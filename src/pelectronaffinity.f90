   IF (ID/=N) CALL PABORT('A BUG')
   DEALLOCATE(LTAKEN,T1,T2,T3) 

   ! LOOP OVER GROUPS
   DO IG=1,NGROUPS
!  DO IG=3,3



   ! INDENTATION SUPPRESSED




!  WRITE(6,'(/,A,20A10:)') '---------',('----------',IB=1,GROUPSIZE(IG))
!  WRITE(6,'(A,I3,A,20I10:)') 'GROUP',IG,':',(GROUPMEMS(IB,IG),IB=1,GROUPSIZE(IG))
