!    WRITE(6,'(A,A)') 'REFERENCE WAVE FUNCTION FOR MBGF STORED IN ',TRIM(COPTN(1))//'.gf0'
!   ENDIF

   ENDDO

   RETURN
END SUBROUTINE



SUBROUTINE FULL_EA_HAMILTONIAN_SCALE(ORDER,SCALEFACTOR)
! FORM FULL HAMILTONIAN AND DIAGONALIZE IT BY THE STANDARD ALGORITHMS.
! CAUTION, DO NOT USE THIS FOR PRODUCTION RUN.

   USE CONTROL
   USE GRADIENT
   USE STRUCTURE
   USE INTEGRAL
   USE BASISSET
   USE FULLCI

   IMPLICIT NONE
   LOGICAL :: LEVEC
   INTEGER :: ORDER
   INTEGER :: IA,IB,IC,ID
   INTEGER :: INFO
   REAL :: MEM,DEV
   DOUBLE PRECISION :: SCALEFACTOR
   DOUBLE PRECISION,ALLOCATABLE :: HAM(:,:)
   DOUBLE PRECISION,ALLOCATABLE :: VL(:,:),VR(:,:),ER(:),EI(:),WK(:)
   DOUBLE PRECISION,ALLOCATABLE :: TRL(:,:),PRD(:,:)

   LEVEC=.FALSE.
   IF (IOPTN(97) > 0) LEVEC=.TRUE.
!  WRITE(6,'(I2,A,I2,A)') ORDER-1,'-HOLE',ORDER,'-PARTICLE CONFIGURATION INTERACTION CALCULATION FOR ALL STATES'
   IF (SCALEFACTOR /= 1.0D0) THEN
!   WRITE(6,'(A)') '*************************************************************'
!   WRITE(6,'(A,F7.4,A)') '* WARNING: SCALE FACTOR FOR FLUCTUATION POTENTIAL = ',SCALEFACTOR,' *'
!   WRITE(6,'(A)') '*************************************************************'
   ENDIF
   IF (LEVEC) THEN 
    MEM=16.0*(2.0*(NCF*EA_NCF)**2+8.0*NCF*EA_NCF)
   ELSE
    MEM=16.0*((NCF*EA_NCF)**2+9.0*NCF*EA_NCF)
   ENDIF
   IF (MEM > 1000000.0) THEN
!   WRITE(6,'(A,F7.1,A)') 'ESTIMATED MEMORY USAGE WILL BE ',MEM/1000000.0,' MB'
   ELSE IF (MEM > 1000.0) THEN
!   WRITE(6,'(A,F7.1,A)') 'ESTIMATED MEMORY USAGE WILL BE ',MEM/1000.0,' KB'
   ELSE
!   WRITE(6,'(A,F7.1,A)') 'ESTIMATED MEMORY USAGE WILL BE ',MEM,' B'
   ENDIF
   IF (MEM > DOPTN(28)*1000000.0) CALL PABORT('OUT OF MEMORY') 
   ALLOCATE(HAM(NCF*EA_NCF,NCF*EA_NCF),VL(1,NCF*EA_NCF),ER(NCF*EA_NCF),EI(NCF*EA_NCF),WK(4*NCF*EA_NCF))
   IF (LEVEC) THEN 
    ALLOCATE(VR(NCF*EA_NCF,NCF*EA_NCF))
   ELSE
    ALLOCATE(VR(1,NCF*EA_NCF))
   ENDIF
   ALLOCATE(TRL(EA_NCF,NCF),PRD(EA_NCF,NCF))

   OPEN(50,FILE=TRIM(COPTN(1))//'.fi0',FORM='UNFORMATTED')
   OPEN(60,FILE=TRIM(COPTN(1))//'.fo0',FORM='UNFORMATTED')

   ! FORM HAMILTONIAN AND KEEP IT IN MEMORY
   DO IA=1,NCF
    DO IB=1,EA_NCF
     TRL=0.0D0
     IF (NORDER(IA)+EA_NORDER(IB) <= ORDER) TRL(IB,IA)=1.0D0
!    REWIND(50)
!    WRITE(50) TRL
!    CALL EA_HAMILTONIAN_PRODUCT(50,60,0,ORDER)
!    REWIND(60)
!    READ(60) TRL
     REWIND(50)
     WRITE(50) TRL
     CALL EA_HAMILTONIAN_PRODUCT(50,60,0,ORDER)
     REWIND(60)
     READ(60) TRL
     CALL H0_PRODUCT(50,60,-1)
     REWIND(60)
     READ(60) PRD
     TRL=PRD+(TRL-PRD)*SCALEFACTOR
     DO IC=1,NCF
      DO ID=1,EA_NCF
       HAM((IA-1)*EA_NCF+IB,(IC-1)*EA_NCF+ID)=TRL(ID,IC)
      ENDDO
     ENDDO
    ENDDO
   ENDDO
!  CALL DUMP5(HAM,NCF*EA_NCF)

   DEALLOCATE(TRL,PRD)
   CLOSE(50)
   CLOSE(60)

   ! DIAGONALIZE HAMILTONIAN
   IF (LEVEC.OR.(IOPTN(97) > 0)) THEN
    CALL DGEEV('N','V',NCF*EA_NCF,HAM,NCF*EA_NCF,ER,EI,VL,1,VR,NCF*EA_NCF,WK,4*NCF*EA_NCF,INFO)
    IF (INFO /= 0) CALL PABORT('DGEEV FAILED TO DIAGONALIZE A MATRIX')
    DEV=0.0D0
    DO IA=1,NCF*EA_NCF
     DEV=DEV+EI(IA)**2
    ENDDO
    IF (DEV > 1.0E-5) THEN
     CALL WARNING('THE IMAGINARY PART OF THE EIGENVALUES IS NOT ZERO')
!    WRITE(6,'(A,F20.10)') 'DEVIATION = ',DEV
    ENDIF
    CALL PIKSRT(NCF*EA_NCF,NCF*EA_NCF,ER,VR,EI)
    DO IA=1,MIN(25,NCF*EA_NCF)
!    WRITE(6,'(A,I3,A,F20.15,A)') 'STATE ',IA,' ENERGY = ', ER(IA)+NUCLEAR_REPULSION,' HARTREE'
!    DO IB=1,NCF*EA_NCF
!     WRITE(6,'(I10,F20.15)') IB,VR(IB,IA)
!    ENDDO
    ENDDO
    IF (IOPTN(97) > 0) THEN
     OPEN(97,FILE=TRIM(COPTN(1))//'.gf2',FORM='UNFORMATTED')
     REWIND(97)
     WRITE(97) ER
     WRITE(97) VR
     CLOSE(97)
!    WRITE(6,'(A,A)') '(N+1)-ELECTRON ENERGIES AND WAVE FUNCTIONS FOR MBGF STORED IN ',TRIM(COPTN(1))//'.gf2'
    ENDIF
   ELSE
    CALL DGEEV('N','N',NCF*EA_NCF,HAM,NCF*EA_NCF,ER,EI,VL,1,VR,1,WK,4*NCF*EA_NCF,INFO)
    IF (INFO /= 0) CALL PABORT('DGEEV FAILED TO DIAGONALIZE A MATRIX')
    DEV=0.0D0
    DO IA=1,NCF*EA_NCF
     DEV=DEV+EI(IA)**2
    ENDDO
    IF (DEV > 1.0E-5) THEN
     CALL WARNING('THE IMAGINARY PART OF THE EIGENVALUES IS NOT ZERO')
!    WRITE(6,'(A,F20.10)') 'DEVIATION = ',DEV
    ENDIF
    CALL PIKSRT_EVALONLY(NCF*EA_NCF,ER)
    DO IA=1,MIN(25,NCF*EA_NCF)
!    WRITE(6,'(A,I3,A,F20.15,A)') 'STATE ',IA,' ENERGY = ', ER(IA)+NUCLEAR_REPULSION,' HARTREE'
    ENDDO
   ENDIF
   CALL PFLUSH(6)

   DEALLOCATE(HAM,VL,VR,ER,EI,WK)

   RETURN
END SUBROUTINE



SUBROUTINE HIGHORDER_MP_EA_ALLSTATES_OBSOLETE(ORDER)
! PERFORM HIGH-ORDER MOELLER-PLESSET PERTURBATION CALCULATIONS IN A RECURSIVE ALGORITHM
! FOR ALL ELECTRON-ATTACHED DETERMINANTS AND STORE THE WAVE FUNCTIONS AND ENERGIES

   USE CONTROL
   USE GRADIENT
   USE STRUCTURE
   USE INTEGRAL
   USE BASISSET
   USE FULLCI

   IMPLICIT NONE
   LOGICAL,PARAMETER :: SILENT = .TRUE.
   INTEGER,PARAMETER :: MAXFILE = 100
   INTEGER :: ORDER
   INTEGER :: MOI,MOJ
   INTEGER :: IA,IB
   INTEGER :: QA,QB,PA,PB
   INTEGER :: I,J,K,IFILE
   REAL :: MEM,ICPUS,ICPUE
   DOUBLE PRECISION :: HA,HB
   DOUBLE PRECISION :: EMP,DMP(0:ORDER),SIGMA(0:ORDER)
   DOUBLE PRECISION,ALLOCATABLE :: VEC1(:,:),VEC2(:,:)

   CALL CPU_TIME(ICPUS)
   IF (.NOT.SILENT) WRITE(6,'(A)') 'RECURSIVE HIGH-ORDER MOELLER-PLESSET PERTURBATION CALCULATIONS WILL BE PERFORMED'

   ALLOCATE(VEC1(EA_NCF,NCF),VEC2(EA_NCF,NCF))

   OPEN(60,FILE=TRIM(COPTN(1))//'.ea0',FORM='UNFORMATTED')
   OPEN(61,FILE=TRIM(COPTN(1))//'.ea1',FORM='UNFORMATTED')
   REWIND(60)
   REWIND(61)

   ! LOOP OVER EA CONFIGURATIONS
   DO PA=1,NCF
    DO PB=1,EA_NCF

     EMP=NUCLEAR_REPULSION
     IF (.NOT.SILENT) THEN
      IF (NORDER(PA)+EA_NORDER(PB)==1) WRITE(6,'(A,I3,A,I1,A,I3,A,I1,A)') &
       'INITIAL EA CONFIGURATION ',PA,' (',NORDER(PA),')',PB,' (',EA_NORDER(PB),') KOOPMANS'
      IF (NORDER(PA)+EA_NORDER(PB)/=1) WRITE(6,'(A,I3,A,I1,A,I3,A,I1,A)') &
       'INITIAL EA CONFIGURATION ',PA,' (',NORDER(PA),')',PB,' (',EA_NORDER(PB),')'
      WRITE(6,'(A)') '--------------------------------------------------------------------------'
      WRITE(6,'(A)') 'ORDER      CORRELATION        TOTAL ENERGY         SELF-ENERGY   CPU / SEC'
     ENDIF
     OPEN(50,FILE=TRIM(COPTN(1))//'.fi0',FORM='UNFORMATTED')
     OPEN(51,FILE=TRIM(COPTN(1))//'.fo0',FORM='UNFORMATTED')

     ! INITIALIZE WAVEFUNCTION TO THE HARTREE-FOCK DETERMINANT
     VEC1=0.0D0
     VEC1(PB,PA)=1.0D0
     REWIND(50)
     WRITE(50) VEC1
!    CALL DUMP14(50,1)

     ! DMP(0)
     CALL H0_PRODUCT(50,51,-1)
     REWIND(51)
     READ(51) VEC2
     DMP(0)=0.0D0
     DO QA=1,NCF
      DO QB=1,EA_NCF
       DMP(0)=DMP(0)+VEC1(QB,QA)*VEC2(QB,QA)
      ENDDO
     ENDDO
     EMP=EMP+DMP(0)
     IF (.NOT.SILENT) WRITE(6,'(I2,F20.10,2F20.10,F12.1)') 0,DMP(0),EMP,EMP-MP_STORED(0),0.0
     SIGMA(0)=EMP-MP_STORED(0)

     ! DMP(1) ONWARD
     DO IFILE=0,ORDER-1
      IF (IOPTN(9) >= 3) THEN
       REWIND(50)
       DO I=0,IFILE
        READ(50) VEC1
       ENDDO
       IF (.NOT.SILENT) WRITE(6,'(I3,A)') IFILE,'TH ORDER WAVE FUNCTION'
       CALL DUMP5(VEC1,NCF)
      ENDIF
      CALL EA_HAMILTONIAN_PRODUCT(50,51,IFILE,MIN(IOCC-ICORE,IALL(0,0,0)-IOCC-IVIRTCORE) &
                                             +MIN(IOCC-ICORE+1,IALL(0,0,0)-IOCC-IVIRTCORE))
      REWIND(51)
      DO I=0,IFILE
       READ(51) VEC1
      ENDDO
      IF (IOPTN(9) >= 3) THEN
       IF (.NOT.SILENT) WRITE(6,'(A,I1,A)') 'H|',IFILE,'>'
       CALL DUMP5(VEC1,NCF)
      ENDIF
      REWIND(50)
      READ(50) VEC2
      DMP(IFILE+1)=0.0D0
      DO QA=1,NCF
       DO QB=1,EA_NCF
        DMP(IFILE+1)=DMP(IFILE+1)+VEC1(QB,QA)*VEC2(QB,QA)
       ENDDO
      ENDDO
      IF (IFILE == 0) DMP(1)=DMP(1)-DMP(0)
      EMP=EMP+DMP(IFILE+1)
      CALL CPU_TIME(ICPUE)
      IF (.NOT.SILENT) WRITE(6,'(I2,F20.10,2F20.10,F12.1)') IFILE+1,DMP(IFILE+1),EMP,EMP-MP_STORED(IFILE+1),ICPUE-ICPUS
      SIGMA(IFILE+1)=EMP-MP_STORED(IFILE+1)
      CALL CPU_TIME(ICPUS)
      CALL PFLUSH(6)
!     IF (DABS(DMP(IFILE+1)) < 1.0D-15) EXIT
      VEC1=-VEC1
      REWIND(50)
      DO K=0,IFILE
       READ(50) VEC2
       DO IA=1,NCF
        HA=0.0D0
        IF (K == IFILE) THEN
         DO I=1,IALL(0,0,0)-IVIRTCORE
          IF (BTEST(CFHALF(IA),I-1)) HA=HA+EPSILON(I,0,0,0)
         ENDDO
        ENDIF
        DO IB=1,EA_NCF
         HB=0.0D0
         IF (K == IFILE) THEN
          DO I=1,IALL(0,0,0)-IVIRTCORE
           IF (BTEST(EA_CFHALF(IB),I-1)) HB=HB+EPSILON(I,0,0,0)
          ENDDO
         ENDIF
         IF (K == IFILE) THEN
          VEC1(IB,IA)=VEC1(IB,IA)+(DMP(IFILE-K+1)+HA+HB)*VEC2(IB,IA)
         ELSE
          VEC1(IB,IA)=VEC1(IB,IA)+DMP(IFILE-K+1)*VEC2(IB,IA)
         ENDIF
        ENDDO
       ENDDO
      ENDDO
      DO IA=1,NCF
       HA=0.0D0
       DO I=1,IALL(0,0,0)-IVIRTCORE
        IF (BTEST(CFHALF(IA),I-1)) HA=HA+EPSILON(I,0,0,0)
       ENDDO
       DO IB=1,EA_NCF
        HB=0.0D0
        DO I=1,IALL(0,0,0)-IVIRTCORE
         IF (BTEST(EA_CFHALF(IB),I-1)) HB=HB+EPSILON(I,0,0,0)
        ENDDO
        IF ((IA == PA).AND.(IB == PB)) THEN
         VEC2(IB,IA)=0.0D0
! SPECULATIVE 
        ELSE IF (DABS(HA+HB-DMP(0)) < 1.0D-10) THEN
         IF (.NOT.SILENT) WRITE(6,'(A,2I3)') 'DEGENERACY DETECTED',IA,IB
         VEC2(IB,IA)=0.0D0
        ELSE
         VEC2(IB,IA)=VEC1(IB,IA)/(HA+HB-DMP(0))
        ENDIF
       ENDDO
      ENDDO
      IF (IFILE == MAXFILE) EXIT
      WRITE(50) VEC2
     ENDDO

     IF (.NOT.SILENT) WRITE(6,'(A)') '--------------------------------------------------------------------------'

     REWIND(50)
     DO I=0,ORDER-1
      IF (I==0) THEN
       WRITE(60) SIGMA(0)
!if (pa==1) write(*,*) sigma(0)
      ELSE
       WRITE(60) SIGMA(I)-SIGMA(I-1)
!if (pa==1) write(*,*) sigma(i)-sigma(i-1)
      ENDIF
      READ(50) VEC1
      WRITE(61) VEC1
     ENDDO

     CLOSE(50)
     CLOSE(51)

    ENDDO
   ENDDO

   DEALLOCATE(VEC1,VEC2)
   CLOSE(60)
   CLOSE(61)

   RETURN
END SUBROUTINE



SUBROUTINE HIGHORDER_MP_EA_ALLSTATES(ORIGINALORDER)
