    WRITE(6,'(A,F7.1,A)') 'ESTIMATED MEMORY USAGE WILL BE ',MEM/1000000.0,' MB'
   ELSE IF (MEM > 1000.0) THEN
    WRITE(6,'(A,F7.1,A)') 'ESTIMATED MEMORY USAGE WILL BE ',MEM/1000.0,' KB'
   ELSE
    WRITE(6,'(A,F7.1,A)') 'ESTIMATED MEMORY USAGE WILL BE ',MEM,' B'
   ENDIF
   IF (MEM > DOPTN(28)*1000000.0) CALL PABORT('OUT OF MEMORY')

   ! LOOP OVER ORBITALS TO BE ADDED AN ELECTRON
   NQP=IOPTN(44)
   IF (NQP < 1) NQP=IALL(0,0,0)-IVIRTCORE-IOCC
   DO MOP=IOCC+1,MIN(IOCC+NQP,IALL(0,0,0)-IVIRTCORE)

    EMP=NUCLEAR_REPULSION
    WRITE(6,'(A,I3)') 'STATE ELECTRON-ATTACHED TO ORBITAL ',MOP
    WRITE(6,'(A)') '--------------------------------------------------------------------------'
    WRITE(6,'(A)') 'ORDER      CORRELATION        TOTAL ENERGY         SELF-ENERGY   CPU / SEC'
    ALLOCATE(VEC1(EA_NCF,NCF),VEC2(EA_NCF,NCF))
    OPEN(50,FILE=TRIM(COPTN(1))//'.fi0',FORM='UNFORMATTED')
    OPEN(51,FILE=TRIM(COPTN(1))//'.fo0',FORM='UNFORMATTED')

    ! INITIALIZE WAVEFUNCTION TO THE HARTREE-FOCK DETERMINANT
    IR=EA_ADDRSS(IBSET(CFHALF(1),MOP-1))
    VEC1=0.0D0
    VEC1(IR,1)=1.0D0
    REWIND(50)
    WRITE(50) VEC1
    !CALL DUMP14(50,1)

    ! DMP(0)
    DMP(0)=0.0D0
    DO MOI=1,IOCC
     DMP(0)=DMP(0)+2.0D0*EPSILON(MOI,0,0,0)
    ENDDO
    DMP(0)=DMP(0)+EPSILON(MOP,0,0,0)
    EMP=EMP+DMP(0)
    WRITE(6,'(I2,F20.10,2F20.10,F12.1)') 0,DMP(0),EMP,EMP-MP_STORED(0),0.0

    ! DMP(1) ONWARD
    DO IFILE=0,ORDER-1
     IF (IOPTN(9) >= 3) THEN
      REWIND(50)
      DO I=0,IFILE
       READ(50) VEC1
      ENDDO
      WRITE(6,'(I3,A)') IFILE,'TH ORDER WAVE FUNCTION'
      CALL DUMP5(VEC1,NCF)
     ENDIF
     DEALLOCATE(VEC1,VEC2)
     CALL EA_HAMILTONIAN_PRODUCT(50,51,IFILE,MIN(IOCC-ICORE,IALL(0,0,0)-IOCC-IVIRTCORE) & 
                                            +MIN(IOCC-ICORE+1,IALL(0,0,0)-IOCC-IVIRTCORE))
     ALLOCATE(VEC1(EA_NCF,NCF),VEC2(EA_NCF,NCF))
     REWIND(51)
     DO I=0,IFILE
      READ(51) VEC1
     ENDDO
     IF (IOPTN(9) >= 3) THEN
      WRITE(6,'(A,I1,A)') 'H|',IFILE,'>'
      CALL DUMP5(VEC1,NCF)
     ENDIF
!    IF (IFILE == 0) THEN
!     DMP(1)=-DMP(0)
!     DO MOI=1,IOCC
!      DMP(1)=DMP(1)+2.0D0*H(MOI,MOI)
!     ENDDO
!     DMP(1)=DMP(1)-H(MOP,MOP)
!     DO MOI=1,IOCC
!      DO MOJ=1,IOCC
!       DMP(1)=DMP(1)+(2.0D0*G(MOI,MOI,MOJ,MOJ)-G(MOI,MOJ,MOJ,MOI))
!      ENDDO
!     ENDDO
!     DO MOJ=1,IOCC
!      DMP(1)=DMP(1)-(G(MOP,MOP,MOJ,MOJ)-0.5D0*G(MOP,MOJ,MOJ,MOP))
!     ENDDO
!    ELSE
!     DMP(IFILE+1)=VEC1(IR,1)
!    ENDIF
     DMP(IFILE+1)=VEC1(IR,1)
     IF (IFILE == 0) DMP(1)=DMP(1)-DMP(0)
     EMP=EMP+DMP(IFILE+1)
     CALL CPU_TIME(ICPUE)
     WRITE(6,'(I2,F20.10,2F20.10,F12.1)') IFILE+1,DMP(IFILE+1),EMP,EMP-MP_STORED(IFILE+1),ICPUE-ICPUS
     CALL CPU_TIME(ICPUS)
     CALL PFLUSH(6)
     IF (DABS(DMP(IFILE+1)) < 1.0D-15) EXIT
     VEC1=-VEC1
     REWIND(50)
     DO K=0,IFILE
      READ(50) VEC2
      DO IA=1,NCF
       HA=0.0D0
       IF (K == IFILE) THEN
        DO I=1,IALL(0,0,0)-IVIRTCORE
         IF (BTEST(CFHALF(IA),I-1)) HA=HA+EPSILON(I,0,0,0)
        ENDDO
       ENDIF
       DO IB=1,EA_NCF
        HB=0.0D0
        IF (K == IFILE) THEN
         DO I=1,IALL(0,0,0)-IVIRTCORE
          IF (BTEST(EA_CFHALF(IB),I-1)) HB=HB+EPSILON(I,0,0,0)
         ENDDO
        ENDIF
        IF (K == IFILE) THEN
         VEC1(IB,IA)=VEC1(IB,IA)+(DMP(IFILE-K+1)+HA+HB)*VEC2(IB,IA)
        ELSE
         VEC1(IB,IA)=VEC1(IB,IA)+DMP(IFILE-K+1)*VEC2(IB,IA)
        ENDIF
       ENDDO
      ENDDO
     ENDDO
     DO IA=1,NCF
      HA=0.0D0
      DO I=1,IALL(0,0,0)-IVIRTCORE
       IF (BTEST(CFHALF(IA),I-1)) HA=HA+EPSILON(I,0,0,0)
      ENDDO
      DO IB=1,EA_NCF
       HB=0.0D0
       DO I=1,IALL(0,0,0)-IVIRTCORE
        IF (BTEST(EA_CFHALF(IB),I-1)) HB=HB+EPSILON(I,0,0,0)
       ENDDO
       IF ((IA == 1).AND.(IB == IR)) THEN
        VEC2(IB,IA)=0.0D0
! SPECULATIVE 
       ELSE IF (DABS(HA+HB-DMP(0)) < 1.0D-10) THEN
        WRITE(6,'(A)') 'DEGENERACY DETECTED'
        VEC2(IB,IA)=0.0D0
       ELSE
        VEC2(IB,IA)=VEC1(IB,IA)/(HA+HB-DMP(0))
       ENDIF
      ENDDO
     ENDDO
     IF (IFILE == MAXFILE) EXIT
     WRITE(50) VEC2
    ENDDO

    WRITE(6,'(A)') '--------------------------------------------------------------------------'
    DEALLOCATE(VEC1,VEC2)
    CLOSE(50)
    CLOSE(51)

!   IF (IOPTN(97) > 0) THEN
!    OPEN(97,FILE=TRIM(COPTN(1))//'.gf0',FORM='UNFORMATTED')
!    VEC2=0.0D0
!    REWIND(50)
