   ENDDO

   ! FIGURE OUT 'r' OF Eq.140
   DO IORDER=0,ORIGINALORDER
    DO IA=1,NG
     IC=0
     DO IB=1,NG
      IF (DABS(EPS(IA,IORDER)-EPS(IB,IORDER)) < TOL) IC=IC+1
     ENDDO
     NDEGEN(IA,IORDER)=IC
    ENDDO
   ENDDO
   DO IORDER=0,ORIGINALORDER
    LNOCHANGE=.TRUE.
    DO IA=1,NG
     IF (NDEGEN(IA,IORDER) > NDEGEN(IA,IORDER+1)) LNOCHANGE=.FALSE.
    ENDDO
    IF (LNOCHANGE) THEN
     IR=IORDER
     EXIT
    ENDIF
   ENDDO
