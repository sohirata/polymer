                 FORCETA  =FORCETA  +DFLOAT(Q2X)*(ATOMY(K)*CS2-ATOMZ(K)*SN2) &
                                    *N2(I1,I2,I3,J1,J2,J3,0)*P_P_HELIX(P_SHELL(I,I1,I2,I3),P_SHELL(J,J1,J2,J3),0,Q1X)
                ENDIF
               ENDIF ! --- HELIX END
              ENDDO
             ENDDO
            ENDDO
           ENDDO
          ENDDO
         ENDDO
        ENDDO
       ENDDO
      ENDDO
      ENDDO
      ENDDO
     ENDDO
    ENDDO
   ENDDO
   ENDDO
   ENDDO
   DEALLOCATE(N1,N2)
   IF ((IOPTN(9) == 3).AND.(MYID == 0)) CALL DUMP2

   RETURN
