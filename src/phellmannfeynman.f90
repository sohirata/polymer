                ELSE IF (EO == 2) THEN
                 FORCEY(K)=FORCEY(K)+CS2*N2(I1,I2,I3,J1,J2,J3,0)*P_P_HELIX(P_SHELL(I,I1,I2,I3),P_SHELL(J,J1,J2,J3),0,Q1X)
